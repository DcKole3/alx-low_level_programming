/**
 * *_memset - fill memory function
 * @s: pointer to memory area.
 * @b: constant used to fill memory
 * @n: no of bytes to fill
 * Return: pointer to memory area s
 */
char *_memset(char *s, char b, unsigned int n)
{
unsigned int i;
for (i = 0; i < n; i++)
{
s[i] = b;
}
return (s);
}
